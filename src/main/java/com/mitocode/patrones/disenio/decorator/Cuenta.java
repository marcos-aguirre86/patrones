package com.mitocode.patrones.disenio.decorator;

public class Cuenta {
	
	private String id;
	private String cliente;
	
	public Cuenta() {}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCliente() {
		return cliente;
	}
	public void setCliente(String cliente) {
		this.cliente = cliente;
	}
	
	

}
